public class EjemploFor {
    
    public static void main(String[] args) {
        
        // Imprimir del 0 al 10
        for (int i = 0; i <= 10; i++){
            System.out.println(i);
        }
        
        // Imprimir del 0 al 20 de 2 en 2
        for (int i = 0; i <= 20; i+=2){
            System.out.println(i);
        }
        
        // Imprimir del 20 al 0
        for (int i = 20; i >= 0; i--){
            System.out.println(i);
        }
        
        
        
    }
    
}
